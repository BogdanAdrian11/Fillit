#include "fillit.h"

void	init_output(t_fout *out)
{
	int	i;
	int	j;

	i = 0;
	while (i < FILLIT_MAX)
	{
		j = 0;
		while (j < FILLIT_MAX)
		{
			(*out).matrix[i][j] = '.';
			j++;
		}
		i++;
	}
}
